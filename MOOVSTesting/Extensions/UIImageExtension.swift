import UIKit

/// Adding custom images

extension UIImage {
    
    
    /// Movie avater image
    class var movieAvatar: UIImage{
        return UIImage(named: "MovieAvatar")!
    }
    
    
    /// Not content image
    class var notContent: UIImage{
        return UIImage(named: "Not_Content")!
    }
}
